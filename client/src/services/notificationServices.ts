import { toast } from 'react-toastify';

export const successNotification = (message:string) => {
    toast.success((message), {
        position: "top-right",
        autoClose: 2500,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
    });
}

